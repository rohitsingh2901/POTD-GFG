    const long long unsigned int MOD = 1000000007;

// Function to return decimal equivalent modulo 1000000007 of binary linked list
long long unsigned int decimalValue(Node *head) {
    long long unsigned int ans = 0;
    
    // Traverse the linked list and calculate decimal equivalent
    while (head) {
        ans = (ans * 2 + head->data) % MOD;
        head = head->next;
    }
    
    return ans;
}

